<?php 
function amount ($arg1, $arg2) {
    $arg3 = $arg1 + $arg2;
    return $arg3;
}

function difference ($arg1, $arg2) {
    $arg3 = $arg1 - $arg2;
    return $arg3;
}

function quotient ($arg1, $arg2) {
    $arg3 = $arg1 / $arg2;
    return $arg3;
}

function multiplication ($arg1, $arg2) {
    $arg3 = $arg1 * $arg2;
    return $arg3;
}

